export function getTextWidth(text) {
  let width = 0;
  for (let i = 0; i < text.length; i++) {
    if (text[i] === ' ') width += 2;
    else if (FONT_DICTIONARY[text[i]]) {
      width += FONT_DICTIONARY[text[i]].w;
      if (i < text.length - 1 && text[i + 1] !== ' ') width += 1;
    }
  }
  return width;
}

export function buildArtGrid(text, totalCols, bgMode) {
  const data = Array.from({ length: 7 }, () =>
    Array.from({ length: totalCols }, () => {
      if (bgMode === 'random') return Math.floor(Math.random() * 3);
      return parseInt(bgMode, 10) || 0;
    })
  );

  const targetText = text.toUpperCase();
  let col = Math.floor((52 - getTextWidth(targetText)) / 2);
  if (col < 0) col = 0;

  for (let i = 0; i < targetText.length; i++) {
    const ch = targetText[i];
    if (ch === ' ') { col += 2; continue; }
    if (!FONT_DICTIONARY[ch]) continue;
    const { w, p } = FONT_DICTIONARY[ch];
    for (let r = 0; r < 7; r++) {
      for (let c = 0; c < w; c++) {
        if (col + c < totalCols && p[r][c]) data[r][col + c] = 4;
      }
    }
    col += w;
    if (i < targetText.length - 1 && targetText[i + 1] !== ' ') col += 1;
  }

  return data;
}

export function getTotalCols(year) {
  const start = new Date(year, 0, 1);
  const end = new Date(year, 11, 31);
  return Math.ceil((Math.ceil((end - start) / (1000 * 60 * 60 * 24)) + 1) / 7);
}
